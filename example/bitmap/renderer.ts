function colorDistance ([red1, green1, blue1]: [number, number, number], [red2, green2, blue2]: [number, number, number]): number {
  return Math.sqrt(
    Math.pow(red2 - red1, 2) +
    Math.pow(green2 - green1, 2) +
    Math.pow(blue2 - blue1, 2)
  )
}

function getXtermNumber ({ red, green, blue }: { red: number, green: number, blue: number }): number {
  const distance = xtermRgb.map((x: [number, number, number]) => {
    return colorDistance(x, [red, green, blue])
  })

  return distance.indexOf(Math.max(...distance))
}

export function printColour (rgb: { red: number, green: number, blue: number }): string {
  return `\x1b[48;5;${getXtermNumber(rgb)}m \x1b[0m`
}
